import mongoose from "mongoose";

const shopListingSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    location: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const ShopListing = mongoose.model("ShopListing", shopListingSchema);
export default ShopListing;
